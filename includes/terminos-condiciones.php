<h1>TÉRMINOS Y CONDICIONES DE SERVICIO – <strong>QuintaMuebles</strong></h1>

<h2>1. Objeto</h2>
<p>
    Por medio de este documento, <strong>QuintaMuebles</strong>, empresa dedicada al diseño, elaboración e instalación de mobiliario personalizado, establece los términos y condiciones que rigen la relación comercial con el CLIENTE al contratar cualquiera de sus productos o servicios. <br><br>

El CLIENTE contrata los servicios para el diseño, fabricación e instalación de muebles según las especificaciones acordadas en la cotización o propuesta comercial aceptada. La aceptación de una cotización o pago inicial implica la aceptación total de estos términos.
</p>

<h2>2. Cotización, precios y pagos</h2>
<p>
    Los precios de los productos y servicios serán los establecidos en la cotización enviada al CLIENTE, la cual tendrá una vigencia de <strong>15 días calendario</strong> a partir de su emisión. <br><br>
    <ul>
        <li>* El valor puede incluir o no el transporte e instalación, según se especifique.</li>
        <li>* Los pagos se realizarán en los plazos acordados, generalmente: <br>
            <strong>50% de anticipo</strong> al iniciar el proyecto y <strong>50% contra entrega</strong>.</li>
        <li>* El incumplimiento en los pagos puede generar intereses de mora según la tasa máxima legal permitida.</li>
        <li>* QuintaMuebles se reserva el derecho de suspender la producción o entrega si existen pagos pendientes.</li>
    </ul>
</p>

<h2>3. Tiempos de entrega</h2>
<p>
    Los plazos de diseño, fabricación y entrega serán los indicados en la propuesta comercial y estarán sujetos a factores como disponibilidad de materiales, volumen de trabajo y condiciones logísticas. <br><br>
    Cualquier modificación en los tiempos será informada oportunamente al CLIENTE.
</p>

<h2>4. Responsabilidades del CLIENTE</h2>
<p>
    El CLIENTE se compromete a: <br><br>
    <ul>
        <li>* Proporcionar las medidas y especificaciones correctamente o permitir la visita técnica para toma de medidas.</li>
        <li>* Facilitar el acceso al lugar de entrega e instalación en la fecha pactada.</li>
        <li>* Aceptar o rechazar los diseños antes de iniciar la fabricación.</li>
        <li>* Asumir costos adicionales derivados de cambios posteriores a la aprobación del diseño.</li>
    </ul>
</p>

<h2>5. Garantía</h2>
<p>
    QuintaMuebles garantiza sus productos contra defectos de fabricación por un período de <strong>1 año</strong> a partir de la fecha de entrega. <br><br>
    Esta garantía <strong>no cubre</strong>: <br><br>
    <ul>
        <li>* Daños por mal uso o manipulación inadecuada.</li>
        <li>* Desgaste natural por el uso.</li>
        <li>* Instalaciones ajenas o alteraciones no autorizadas.</li>
    </ul>
</p>

<h2>6. Cancelaciones y devoluciones</h2>
<p>
    Una vez aprobado el diseño y realizado el anticipo, no se aceptan devoluciones ni reembolsos, debido a que se trata de productos fabricados a medida. <br><br>
    Si el CLIENTE decide cancelar el proyecto antes del inicio de fabricación, podrá solicitar la devolución del anticipo <strong>descontando los gastos de diseño, materiales o logística incurridos hasta ese momento</strong>.
</p>

